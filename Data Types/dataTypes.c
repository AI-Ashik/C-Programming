// This is for the basic Idea of DataTypes in C Programming

// Create variables
int myNum = 5;             // Integer (whole number)
float myFloatNum = 5.99;   // Floating point number
char myLetter = 'd';       // Character

// Print variables
printf("%d\n", myNum); // %d or %i
printf("%f\n", myFloatNum); // %f
printf("%c\n", myLetter); // %c


